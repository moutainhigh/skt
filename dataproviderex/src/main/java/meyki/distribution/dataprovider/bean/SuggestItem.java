package meyki.distribution.dataprovider.bean;

public class SuggestItem {
	private String id ;
	private String title ;//标题
	private String add_time ;//2015-07-22 16:49:24
	private String comment_nums ;//评论总数 
	private String zan_nums ;//赞总数
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getAdd_time() {
		return add_time;
	}
	public void setAdd_time(String add_time) {
		this.add_time = add_time;
	}
	public String getComment_nums() {
		return comment_nums;
	}
	public void setComment_nums(String comment_nums) {
		this.comment_nums = comment_nums;
	}
	public String getZan_nums() {
		return zan_nums;
	}
	public void setZan_nums(String zan_nums) {
		this.zan_nums = zan_nums;
	}
	
	
}
